public class Prestamo {
Double ValorPrestamo;
Double TasaInteres;
int TiempoMeses;
String Amortizacion;
Double SaldoRestante;
Double Cuotas;
String TipoPrestamo;

    public Prestamo(Double valorPrestamo, Double tasaInteres, int tiempoMeses, String amortizacion, Double saldoRestante, Double cuotas, String tipoPrestamo) {
        ValorPrestamo = valorPrestamo;
        TasaInteres = tasaInteres;
        TiempoMeses = tiempoMeses;
        Amortizacion = amortizacion;
        SaldoRestante = saldoRestante;
        Cuotas = cuotas;
        TipoPrestamo = tipoPrestamo;
    }




}
